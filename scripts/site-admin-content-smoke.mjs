#!/usr/bin/env node

// End-to-end smoke against the Posts/Pages/Assets CRUD endpoints.
// Reuses the same next-auth cookie-minting pattern as site-admin-write-smoke.mjs
// so it only needs the NEXTAUTH_SECRET + SITE_ADMIN_GITHUB_USERS already in .env.
//
// Usage:
//   npm run smoke:site-admin:content                # against http://127.0.0.1:3000
//   BASE_URL=https://staging.jinkunchen.com npm run smoke:site-admin:content

import { loadProjectEnv } from "./load-project-env.mjs";

loadProjectEnv({ override: false });

const DEFAULT_BASE_URL = "http://127.0.0.1:3000";

function asString(value) {
  return typeof value === "string" ? value.trim() : "";
}

function firstGithubUserFromCsv(raw) {
  const users = String(raw || "")
    .split(",")
    .map((item) => item.trim().toLowerCase())
    .filter(Boolean);
  return users[0] || "";
}

async function buildAutoSiteAdminCookie() {
  const secret = asString(process.env.NEXTAUTH_SECRET || process.env.AUTH_SECRET || "");
  const login = firstGithubUserFromCsv(process.env.SITE_ADMIN_GITHUB_USERS || "");
  if (!secret || !login) return "";
  try {
    const { encode } = await import("next-auth/jwt");
    const token = await encode({
      secret,
      token: {
        sub: `content-smoke-${login}`,
        login,
        name: login,
      },
      maxAge: 60 * 30,
    });
    if (!token) return "";
    return `__Secure-next-auth.session-token=${token}; next-auth.session-token=${token}`;
  } catch (err) {
    throw new Error(`failed to mint cookie: ${String(err)}`);
  }
}

async function requestJson({ method, url, cookie, body }) {
  const headers = {};
  if (cookie) headers.Cookie = cookie;
  if (body !== undefined) headers["Content-Type"] = "application/json";
  const res = await fetch(url, {
    method,
    headers,
    ...(body !== undefined ? { body: JSON.stringify(body) } : {}),
    cache: "no-store",
  });
  const text = await res.text().catch(() => "");
  let json = null;
  try {
    json = text ? JSON.parse(text) : null;
  } catch {
    json = null;
  }
  return { status: res.status, ok: res.ok, text, json };
}

function unwrap(raw) {
  if (!raw || typeof raw !== "object" || Array.isArray(raw)) return raw;
  return "data" in raw ? raw.data : raw;
}

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function describe(title) {
  console.log(`\n>>> ${title}`);
}

function pad2(n) {
  return String(n).padStart(2, "0");
}

function makeSampleSlug(prefix) {
  const d = new Date();
  return `${prefix}-${d.getUTCFullYear()}${pad2(d.getUTCMonth() + 1)}${pad2(d.getUTCDate())}-${pad2(d.getUTCHours())}${pad2(d.getUTCMinutes())}${pad2(d.getUTCSeconds())}`;
}

function makeSamplePostSource(title) {
  return `---
title: ${title}
date: ${new Date().toISOString().slice(0, 10)}
draft: true
tags:
  - smoke
---

# ${title}

Smoke-test fixture generated by site-admin-content-smoke at ${new Date().toISOString()}.
This post is created, updated, then deleted — it should not survive the run.
`;
}

function makeSamplePageSource(title) {
  return `---
title: ${title}
draft: true
---

Smoke-test page body created by site-admin-content-smoke.
`;
}

const TINY_PNG_BYTES = Uint8Array.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d,
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1f, 0x15, 0xc4, 0x89, 0x00, 0x00, 0x00,
  0x0d, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9c, 0x63, 0xfa, 0xcf, 0xc0, 0x00,
  0x00, 0x00, 0x03, 0x00, 0x01, 0x8d, 0x9c, 0x73, 0x8e, 0x00, 0x00, 0x00,
  0x00, 0x49, 0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82,
]);

async function run() {
  const baseUrl = (process.env.BASE_URL || DEFAULT_BASE_URL).replace(/\/+$/, "");
  const cookie = await buildAutoSiteAdminCookie();
  if (!cookie) {
    console.error(
      "[content-smoke] NEXTAUTH_SECRET + SITE_ADMIN_GITHUB_USERS are required to mint a dev cookie.",
    );
    console.error(
      "[content-smoke] Set them in .env (or .env.local) and re-run.",
    );
    process.exit(78);
  }

  describe(`Baseline against ${baseUrl}`);

  const postsSlug = makeSampleSlug("smoke-post");
  const pagesSlug = makeSampleSlug("smoke-page");

  try {
    // ---- Posts ---------------------------------------------------------
    describe("POST /api/site-admin/posts (create)");
    const createPost = await requestJson({
      method: "POST",
      url: `${baseUrl}/api/site-admin/posts`,
      cookie,
      body: { slug: postsSlug, source: makeSamplePostSource("Smoke Post") },
    });
    assert(
      createPost.status === 201 && createPost.json?.ok === true,
      `create post failed: status=${createPost.status} body=${createPost.text}`,
    );
    const createdPost = unwrap(createPost.json);
    console.log(`   created: ${createdPost.href} @ ${createdPost.version.slice(0, 12)}`);

    describe("GET /api/site-admin/posts?drafts=1");
    const listPosts = await requestJson({
      method: "GET",
      url: `${baseUrl}/api/site-admin/posts?drafts=1`,
      cookie,
    });
    assert(listPosts.ok && listPosts.json?.ok === true, "list posts failed");
    const posts = unwrap(listPosts.json).posts;
    assert(
      posts.some((p) => p.slug === postsSlug),
      `created post ${postsSlug} missing from listing`,
    );
    console.log(`   list size: ${posts.length}`);

    describe(`GET /api/site-admin/posts/${postsSlug}`);
    const readPost = await requestJson({
      method: "GET",
      url: `${baseUrl}/api/site-admin/posts/${postsSlug}`,
      cookie,
    });
    assert(readPost.ok && readPost.json?.ok === true, "read post failed");
    const readPostData = unwrap(readPost.json);
    assert(
      readPostData.source.includes("Smoke Post"),
      "read post source missing body",
    );

    describe(`PATCH /api/site-admin/posts/${postsSlug}`);
    const updatedSource = makeSamplePostSource("Smoke Post (updated)");
    const patchPost = await requestJson({
      method: "PATCH",
      url: `${baseUrl}/api/site-admin/posts/${postsSlug}`,
      cookie,
      body: { source: updatedSource, version: createdPost.version },
    });
    assert(
      patchPost.ok && patchPost.json?.ok === true,
      `patch post failed: body=${patchPost.text}`,
    );
    const patchedPost = unwrap(patchPost.json);
    assert(
      patchedPost.version !== createdPost.version,
      "patch post: version should change",
    );
    console.log(`   patched: new version ${patchedPost.version.slice(0, 12)}`);

    describe(`PATCH conflict detection (reject stale version)`);
    const staleReject = await requestJson({
      method: "PATCH",
      url: `${baseUrl}/api/site-admin/posts/${postsSlug}`,
      cookie,
      body: { source: updatedSource, version: createdPost.version },
    });
    assert(
      staleReject.status === 409 &&
        staleReject.json?.code === "SOURCE_CONFLICT",
      `expected 409 SOURCE_CONFLICT, got status=${staleReject.status} code=${staleReject.json?.code}`,
    );
    console.log(`   conflict correctly rejected (409 SOURCE_CONFLICT)`);

    describe(`DELETE /api/site-admin/posts/${postsSlug}`);
    const deletePost = await requestJson({
      method: "DELETE",
      url: `${baseUrl}/api/site-admin/posts/${postsSlug}`,
      cookie,
      body: { version: patchedPost.version },
    });
    assert(deletePost.ok, `delete post failed: body=${deletePost.text}`);
    console.log(`   deleted`);

    // ---- Pages ---------------------------------------------------------
    describe("POST /api/site-admin/pages (create)");
    const createPage = await requestJson({
      method: "POST",
      url: `${baseUrl}/api/site-admin/pages`,
      cookie,
      body: { slug: pagesSlug, source: makeSamplePageSource("Smoke Page") },
    });
    assert(
      createPage.status === 201 && createPage.json?.ok === true,
      `create page failed: status=${createPage.status} body=${createPage.text}`,
    );
    const createdPage = unwrap(createPage.json);
    console.log(`   created: ${createdPage.href} @ ${createdPage.version.slice(0, 12)}`);

    describe(`GET /api/site-admin/pages/${pagesSlug}`);
    const readPage = await requestJson({
      method: "GET",
      url: `${baseUrl}/api/site-admin/pages/${pagesSlug}`,
      cookie,
    });
    assert(readPage.ok && readPage.json?.ok === true, "read page failed");

    describe(`DELETE /api/site-admin/pages/${pagesSlug}`);
    const deletePage = await requestJson({
      method: "DELETE",
      url: `${baseUrl}/api/site-admin/pages/${pagesSlug}`,
      cookie,
      body: { version: createdPage.version },
    });
    assert(deletePage.ok, `delete page failed: body=${deletePage.text}`);
    console.log(`   deleted`);

    // ---- Assets --------------------------------------------------------
    describe("POST /api/site-admin/assets (upload 1x1 PNG)");
    const uploadOne = await requestJson({
      method: "POST",
      url: `${baseUrl}/api/site-admin/assets`,
      cookie,
      body: {
        filename: "pixel.png",
        contentType: "image/png",
        base64: Buffer.from(TINY_PNG_BYTES).toString("base64"),
      },
    });
    assert(
      uploadOne.status === 201 && uploadOne.json?.ok === true,
      `asset upload failed: status=${uploadOne.status} body=${uploadOne.text}`,
    );
    const asset1 = unwrap(uploadOne.json);
    console.log(`   uploaded: ${asset1.url} (${asset1.size} bytes)`);

    describe("POST /api/site-admin/assets (dedupe on identical bytes)");
    const uploadTwo = await requestJson({
      method: "POST",
      url: `${baseUrl}/api/site-admin/assets`,
      cookie,
      body: {
        filename: "different-name.png",
        contentType: "image/png",
        base64: Buffer.from(TINY_PNG_BYTES).toString("base64"),
      },
    });
    assert(
      uploadTwo.ok && uploadTwo.json?.ok === true,
      `second asset upload failed: body=${uploadTwo.text}`,
    );
    const asset2 = unwrap(uploadTwo.json);
    assert(
      asset1.url === asset2.url,
      `dedupe failed: first url ${asset1.url} != second ${asset2.url}`,
    );
    console.log(`   dedupe OK (same URL for identical bytes)`);

    describe("POST /api/site-admin/assets (reject unsupported mime)");
    const uploadBadMime = await requestJson({
      method: "POST",
      url: `${baseUrl}/api/site-admin/assets`,
      cookie,
      body: {
        filename: "fake.pdf",
        contentType: "application/pdf",
        base64: Buffer.from("pdf bytes").toString("base64"),
      },
    });
    assert(
      uploadBadMime.status === 400,
      `expected 400 for disallowed mime, got ${uploadBadMime.status}`,
    );
    console.log(`   bad mime correctly rejected (400)`);

    describe("POST /api/site-admin/assets (reject >5MB)");
    const bigPayload = Buffer.alloc(5 * 1024 * 1024 + 32, 0x61);
    const uploadBig = await requestJson({
      method: "POST",
      url: `${baseUrl}/api/site-admin/assets`,
      cookie,
      body: {
        filename: "big.png",
        contentType: "image/png",
        base64: bigPayload.toString("base64"),
      },
    });
    assert(
      uploadBig.status === 400,
      `expected 400 for oversize payload, got ${uploadBig.status}`,
    );
    console.log(`   oversize correctly rejected (400)`);

    console.log("\n✅ content-smoke passed");
  } catch (err) {
    console.error("\n❌ content-smoke failed:", err?.message || err);
    process.exit(1);
  }
}

await run();
